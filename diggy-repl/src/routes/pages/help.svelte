<h2>Help</h2>

<h3>How do I select a language?</h3>

<p>
  Diggy will automatically select a language based on the extension, so <code
    >.py</code
  >
  is Python, <code>.rb</code> is Ruby, etc.
</p>

<h3>X doesn't work!</h3>

<p>
  Perhaps, you can <a href="/pages/contact">send me a message</a>
  or <a href="https://github.com/diggyhq/shell/issues">file a defect</a>. I will
  see what I can do.
</p>

<h3>X is not supported</h3>

<p>
  Diggy is a one-man project. If it is not something that I have in my <a
    href="https://github.com/diggyhq/shell/blob/master/ROADMAP.md">roadmap</a
  >, it is unlikely I will have resources to do this. However, Diggy is
  <a href="https://github.com/diggyhq/shell">an open-source project</a>, and you
  can contribute!
</p>

